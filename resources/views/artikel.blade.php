    <h2>Pengertian Framework</h2>
    <p>Sesuai dengan namanya sendiri, framework adalah kerangka kerja untuk mengembangkan aplikasi berbasis website maupun desktop. Kerangka kerja disini sangat membantu developer dalam menuliskan sebuah dengan lebih terstruktur dan tersusun rapi.

Kerangka kerja diciptakan untuk mempermudah kinerja dari programmer. Sehingga, seorang programmer tidak perlu untuk menuliskan kode secara berulang – ulang. Karena di dalamnya sendiri anda hanya perlu menyusun komponen – komponen pemrograman saja.</p>
    <hr>
    <h2>Fungsi Framework</h2>
    <p>Sebagai developer, tentu harus mengetahui tujuan dari penggunaannya untuk kepentingan pembuatan aplikasi. Sehingga, untuk proses pengerjaan aplikasi dapat dilakukan dengan menggunakan framework yang tepat dan sesuai dengan kebutuhan project. Berikut merupakan beberapa fungsi kerangka kerja dalam web development.</p>
    <hr>
    <h2>Jenis Framework untuk Web Development</h2>
    <p>Setelah mengenal apa itu framework beserta fungsinya, selanjutnya kita akan mengenal beberapa contoh yang digunakan dalam pengembangan website. Berikut ini merupakan beberapa jenis framework meliputi CSS, JavaScript, dan PHP. Dan biasanya sering digunakan dalam pengembangan sebuah website.</p>
